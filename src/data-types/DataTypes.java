class DataTypes {
  static public void main(String[] args) {
    // Java supports two types of data:
    // - Primitive.
    // - Non-primitive.

    // The following are examples of primitive data.
    char a = 'A';
    System.out.println(a);

    // The 'byte' type is used to store numeric values up to 1 byte in length.
    byte x = 1;
    System.out.println(x);

    // The 'short' type is used to store numeric values up to 2 bytes in length.
    short y = 256;
    System.out.println(y);

    // The 'int' type is used to store numeric values up to 4 bytes in length.
    int z = 65536;
    System.out.println(z);

    // The 'long' type is used to store numeric values up to 8 bytes in length.
    // This includes numbers as large as 2,147,438,647, and as small as
    // -2,147,438,658.
    long zz = 2147483647;
    System.out.println(zz);
  }
}
