class DataTypes {
  static public void main(String[] args) {
    // Java supports two types of data:
    // - Primitive.
    // - Non-primitive.

    // The following are examples of primitive data.
    char a = 'A';
    System.out.println(a);

    // THe 'byte' type is used to store numeric values up to 1 byte in length.
    byte x = 1;
    System.out.println(x);

    // THe 'int' type is used to store numeric values up to 4 bytes in length.
    int z = 65536;
    System.out.println(z);
  }
}
